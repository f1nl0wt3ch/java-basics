package vjb.de.vietjob.bean.partneri;

public class PartneriImpl implements Partneri {
      private int id;
      private String nimi;
      private String osoite;
      private String puh;
      private String websivu;
      private String sposti;
      
      
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNimi() {
		return nimi;
	}
	public void setNimi(String nimi) {
		this.nimi = nimi;
	}
	public String getOsoite() {
		return osoite;
	}
	public void setOsoite(String osoite) {
		this.osoite = osoite;
	}
	public String getPuh() {
		return puh;
	}
	public void setPuh(String puh) {
		this.puh = puh;
	}
	public String getWebsivu() {
		return websivu;
	}
	public void setWebsivu(String websivu) {
		this.websivu = websivu;
	}
	public String getSposti() {
		return sposti;
	}
	public void setSposti(String sposti) {
		this.sposti = sposti;
	}
      
      
}
